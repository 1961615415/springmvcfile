public class JAvaOper02 {
    public static void main(String[] args) {
        int b=10;
        int a=1;
        a+=b;
        System.out.println("a="+a);
        System.out.println("b="+b);
        a+=b+1;
        System.out.println("a="+a);
        System.out.println("b="+b);
        int c=10;
        int d=1;
        d*=c+2;
        System.out.println("d="+d);

    }
}
