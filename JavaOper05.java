public class JavaOper05 {
    public static void main(String[] args) {
        //测试位运算符
        int a=7;
        int b=8;
        int c=5;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~b);
        System.out.println(10>>1);
        System.out.println(c<<1);

    }
}
