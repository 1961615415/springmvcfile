public class JavaOper04 {
    public static void main(String[] args) {
        //测试逻辑运算符
        boolean a=true;
        boolean b=false;
        System.out.println(a&b);
        System.out.println(a&&b);
        System.out.println(a|b);
        System.out.println(a||b);
        System.out.println(!b);
        System.out.println(a^b);

    }
}
