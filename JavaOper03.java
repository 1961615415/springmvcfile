public class JavaOper03 {
    public static void main(String[] args) {
        //测试关系运算符
        int a=10;
        int b=19;
        System.out.println(a==b);
        char m='m';
        char n='n';
        System.out.println(m==n);
        System.out.println((int)m);//109
        System.out.println((int)n);//110
    }
}
